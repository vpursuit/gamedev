package de.anschalter.graphics;

import de.anschalter.dna.framework.SimulationTime;
import de.anschalter.gaming.framework.impl.Vect3D;

public interface IPhysicalSimulator {

	/**
	 * Updates this Sprite's Animation and its position based on the velocity.
	 */
	public abstract void update(SimulationTime time);

	public abstract Vect3D getVolume();

	public abstract void setPosition(Vect3D v);

	public abstract Vect3D getPosition();

	public abstract void setVelocity(Vect3D v);

	public abstract Vect3D getVelocity();

}